export const getNewsByCategory = (category: AINewsItem['category']): AINewsItem[] =>
  AI_NEWS.filter(n => n.category === category);

export const getNewsByProvider = (providerId: string): AINewsItem[] =>
  AI_NEWS.filter(n => n.relatedProviders?.includes(providerId));

export const getNewsByModel = (modelId: string): AINewsItem[] =>
  AI_NEWS.filter(n => n.relatedModels?.includes(modelId));

export const getHighImpactNews = (): AINewsItem[] =>
  AI_NEWS.filter(n => n.impact === 'high').sort((a, b) => b.date.localeCompare(a.date));
